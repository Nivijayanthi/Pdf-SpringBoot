package com.bhaiti.beans;

public class CVDetails {
	
	String foNumber;
	String customer;
	String size;
	String iaNumber;
	String statusCode;
	String statusMessage;
	
	public String getStatusMessage() {
		return statusMessage;
	}
	public void setStatusMessage(String statusMessage) {
		this.statusMessage = statusMessage;
	}
	public String getStatusCode() {
		return statusCode;
	}
	public void setStatusCode(String statusCode) {
		this.statusCode = statusCode;
	}
	public String getFoNumber() {
		return foNumber;
	}
	public void setFoNumber(String foNumber) {
		this.foNumber = foNumber;
	}
	public String getCustomer() {
		return customer;
	}
	public void setCustomer(String customer) {
		this.customer = customer;
	}
	public String getSize() {
		return size;
	}
	public void setSize(String size) {
		this.size = size;
	}
	public String getIaNumber() {
		return iaNumber;
	}
	public void setIaNumber(String iaNumber) {
		this.iaNumber = iaNumber;
	}
	
	

}
